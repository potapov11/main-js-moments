// Условие задачи (на русском)
// Что у нас есть:

// Есть исходное сообщение, которое состоит только из обычных символов (буквы, цифры, возможно знаки препинания, но точно не содержит специальные символы: % $ & / # · @ | º \ ª и не содержит пробелы и табуляции).

// Наша машина для передачи сообщений сломана и добавляет шум — то есть вставляет в сообщение лишние символы из этого набора: % $ & / # · @ | º \ ª

// Что нужно сделать:
// Написать функцию, которая удаляет весь шум (эти специальные символы) из полученного сообщения и возвращает чистое исходное сообщение.

// Важно:
// Другие символы (не из этого набора) считаются частью исходного сообщения и их удалять нельзя.
// Пробелов и табуляций в исходном сообщении нет, но если они вдруг появятся — их тоже считаем шумом.

function removeNoise(str) {
	const noiseSymbols = ['%', '$', '&', '/', '#', '·', '@', '|', 'º', '\\', 'ª'];

	let resultString = '';

	for (let char of str) {
		if (!noiseSymbols.includes(char)) {
			resultString += char;
		}
	}

	return resultString;
}

