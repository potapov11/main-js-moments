// ЗАДАНИЕ: мини-система управления заказами в интернет-магазине (ООП + продвинутый JS)
//
// Цель: потренировать классы, приватные поля/методы (#field), работу с Map, async/await,
// инкапсуляцию и разделение ответственности, как в файле forReadCodeOOP.js.
//
// 1. Класс Product
//    - Поля: id, name, price.
//    - Приватное поле #createdAt (строка даты).
//    - Конструктор принимает объект с полями { id, name, price }.
//    - Метод toJSON() возвращает “чистый” объект без приватных полей:
//        { id, name, price, createdAt }  (createdAt можно получить из приватного поля).
//
// 2. Класс CartItem
//    - Поля: product (экземпляр Product), quantity (число).
//    - Приватное поле #totalPrice, которое всегда равно product.price * quantity.
//    - Конструктор принимает { product, quantity }.
//    - Метод updateQuantity(newQty):
//        - обновляет quantity;
//        - пересчитывает #totalPrice.
//    - Геттер total, который возвращает текущее значение #totalPrice.
//
// 3. Класс Cart
//    - Приватные поля:
//        #items — Map, где ключ = product.id, значение = CartItem.
//        #discountPercent — число (0–100), скидка на всю корзину.
//    - Публичные методы:
//        addProduct(product, qty = 1)
//          - если товара ещё нет в корзине — создаёт новый CartItem;
//          - если есть — увеличивает quantity через метод CartItem.updateQuantity.
//        removeProduct(productId)
//          - удаляет товар из #items.
//        setDiscount(percent)
//          - устанавливает скидку на всю корзину (например, 10 означает 10%).
//        getItems()
//          - возвращает массив “чистых” объектов:
//              { id, name, price, quantity, total }.
//        getTotal()
//          - возвращает итоговую сумму всех позиций с учётом скидки.
//    - ВАЖНО:
//        - внешнему коду нельзя обращаться к #items или #discountPercent напрямую;
//        - вся работа должна идти только через публичные методы.
//
// 4. Класс OrderService
//    - Приватные поля:
//        #cart — экземпляр Cart;
//        #history — массив завершённых заказов.
//    - Публичные методы:
//        async checkout()
//          - имитирует оформление заказа:
//              * берёт текущие позиции из корзины (cart.getItems());
//              * считает итоговую сумму (cart.getTotal());
//              * создаёт объект заказа: { id, createdAt, items, total };
//              * добавляет его в #history;
//              * очищает корзину (вариант: пересоздать Cart или добавить метод clear()).
//          - должен быть async и имитировать задержку (через Promise + setTimeout),
//            аналогично тому, как это сделано в FakeUserApi.#delay.
//        getHistory()
//          - возвращает массив заказов (можно как есть, либо копию).
//
// 5. Имитация использования (как функция main в forReadCodeOOP.js)
//    - Внизу файла написать async-функцию main():
//        * создать несколько продуктов (Product);
//        * создать Cart и OrderService;
//        * добавить товары в корзину разными количествами;
//        * установить скидку;
//        * вызвать await orderService.checkout();
//        * вывести в консоль:
//            - состояние корзины до checkout (getItems, getTotal);
//            - историю заказов после checkout (getHistory).
//    - Вызвать main().catch(...) в самом низу файла.
//
// 6. Обязательные фишки JS, которые нужно использовать
//    - Классы с приватными полями/методами: #field / #method().
//    - async/await + Promise + setTimeout для имитации задержки.
//    - Map для хранения элементов корзины.
//    - Геттеры (например, total в CartItem, возможно геттер total в Cart).
//    - Как минимум одна вспомогательная функция или мини-класс (например, генератор id или простой логгер).
