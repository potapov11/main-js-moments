// Факториал — это математическая функция, обозначаемая символом n!,
// которая определяет произведение всех положительных целых чисел от 1 до n

// 5!=5×4×3×2×1=120

const factorial = (n) => {
	if (n === 1) return 1; // Базовый случай: факториал 1 равен 1

	return n * factorial(n - 1); // Рекурсивный вызов
};

console.log(factorial(5));

// Пошаговое выполнение
// Вызов factorial(5):

// n равно 5, не равен 1, переходим к рекурсивному вызову.
// Вычисляем: 5 * factorial(4).
// Вызов factorial(4):

// n равно 4, не равен 1, переходим к рекурсивному вызову.
// Вычисляем: 4 * factorial(3).
// Вызов factorial(3):

// n равно 3, не равен 1, переходим к рекурсивному вызову.
// Вычисляем: 3 * factorial(2).
// Вызов factorial(2):

// n равно 2, не равен 1, переходим к рекурсивному вызову.
// Вычисляем: 2 * factorial(1).
// Вызов factorial(1):

// n равно 1, возвращаем 1 (базовый случай).
// Сбор результатов
// Теперь собираем результаты:

// factorial(1) возвращает 1.
// factorial(2) возвращает 2 * 1 = 2.
// factorial(3) возвращает 3 * 2 = 6.
// factorial(4) возвращает 4 * 6 = 24.
// factorial(5) возвращает 5 * 24 = 120.
// Результат
// Таким образом, console.log(factorial(5)); выведет 120.
