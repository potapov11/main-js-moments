// Дано неотрицательное целое число x. Нужно вернуть квадратный корень из x,
// округленный вниз до ближайшего целого числа (то есть отбрасываем дробную часть). Возвращаемое число также должно быть неотрицательным.

// Как вычислить без встроенных функций:

// Способ 1 — перебор (линейный):
// Проверяем все числа подряд: 0*0, 1*1, 2*2, 3*3... пока результат не станет больше x.
// Для x = 8:

// 0*0 = 0 ≤ 8

// 1*1 = 1 ≤ 8

// 2*2 = 4 ≤ 8

// 3*3 = 9 > 8 → значит ответ 2

// Способ 2 — бинарный поиск (эффективнее):
// Ищем число в диапазоне от 0 до x, проверяя середину.
// Если mid² ≤ x — идем в правую половину (ищем больше)
// Если mid² > x — идем в левую половину (ищем меньше)

// 12345678;

var mySqrt = function (x) {
	let start = 0;
	let end = x;
	let result = 0;

	while (start <= end) {
		const middle = Math.floor((start + end) / 2);
		const squaredMiddle = middle * middle;

		if (squaredMiddle === x) return middle;

		if (squaredMiddle > x) {
			end = middle - 1;
		} else {
			start = middle + 1;
			result = middle;
		}
	}

	return result;
};

console.log(mySqrt(8));
