// Описание: дан массив положительных целых чисел nums и положительное целое число target. Нужно найти минимальную длину непрерывного подмассива, сумма элементов которого больше или равна target. Если такого подмассива нет, следует вернуть 0.
// neetcode.io
// dev.to
// Пример: target = 7, nums = [2,3,1,2,4,3]. Ответ: 2. Подмассив [4,3] имеет минимальную длину в рамках условий задачи.
// dev.to
// Ещё несколько примеров:
// target = 4, nums = [1,4,4] — ответ: 1;
// target = 11, nums = [1,1,1,1,1,1,1,1] — ответ: 0.

const nums = [2, 3, 1, 2, 4, 3],
	target = 7;

//Используется алгоритм скользящего окна,
// Устанавливаем в 0 start и end, увеличиваем end
// на каждом шаге проверяем currentSum, и если она больше target то уменьшаем окно слева
// и записываем сумму длины в min
// алгоритм считается незаконченным пока end < array.length

function getMinLengthSubArray(array, target) {
	let start = 0;
	let end = 0;
	let min = array.length;
	let currentSum = 0;

	while (end < array.length) {
		currentSum += array[end];

		while (currentSum >= target) {
			let currentLength = end - start + 1;

			if (currentLength < min) {
				min = currentLength;
			}

			currentSum -= array[start];
			start++;
		}

		end += 1;
	}

	return min === null ? -1 : min;
}

console.log(getMinLengthSubArray(nums, target));

